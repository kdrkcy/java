
public class elseif {

		public static void main(String[] args) {
			int age=60;
			if (age>=60) {
				System.out.println("you are a senior citizen");
			}
			else if(age>=50) {
				System.out.println("you are at your 50s");
			}
			else if(age>=40) {
				System.out.println("you are at your 40s");
			}
			else {
				System.out.println("you are young");
			}
				
		}
}
